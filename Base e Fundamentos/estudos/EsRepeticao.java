package sejaBemVindo;

public class EsRepeticao {
	
	public static void main(String[] args) {
		
		//ESTRUTURAS DE REPETICAO - LACOS - BOT
		
		//WHILE bot
		
		int contador=0;
		while(contador <= 5){
			System.out.print(contador);
			contador += 1;
		}
		
		System.out.println();
			
		//FOR bot
		
		for (int c=0; c<=5; c++){
			System.out.print(c);
		}
		
		System.out.println();
		
	}
}