package sejaBemVindo;

public class OpRelacionais {
	
	public static void main(String[] args) {
		
		//OPERADORES RELACIONAIS
		
		int vUm = 34;
		int vDois = 56;
		
		System.out.println(vUm == vDois);
		System.out.println(vUm != vDois);
		System.out.println(vUm < vDois);
		System.out.println(vUm > vDois);
		System.out.println(vUm <= vDois);
		System.out.println(vUm >= vDois);
		
	}
}